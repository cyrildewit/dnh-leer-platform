export default {
  name: 'Login',

  auth: 'guest',

  layout: 'BrandDefault',

  data: () => ({
    form: {
      email: 'cyril@example.com',
      password: 'secret',
    },
    remember: false,
  }),

  methods: {
    async login() {
      this.$auth.loginWith('userApi', {
